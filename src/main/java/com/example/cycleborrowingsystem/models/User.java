package com.example.cycleborrowingsystem.models;

/**
 * Minimal User model used by the UI/session. Uses a numeric id to match DAO expectations.
 */
public class User {
    private final long id;
    private String name;
    private String email;

    public User(long id, String name, String email) {
        this.id = id;
        this.name = name;
        this.email = email;
    }

    public long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
